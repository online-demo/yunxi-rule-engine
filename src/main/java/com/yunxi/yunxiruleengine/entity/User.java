package com.yunxi.yunxiruleengine.entity;

public class User {

    /**
     * 姓名
     */
    private String name;

    /**
     * 用户级别
     */
    private int level;
  
    public String getName() {  
        return name;  
    }  
  
    public void setName(String name) {  
        this.name = name;  
    }  
  
    public int getLevel() {  
        return level;  
    }  
  
    public void setLevel(int level) {  
        this.level = level;  
    }  
      
      
      
}  